    public SettingsFrame() {
        initComponents();
        setVisible(true);

        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        setLocationRelativeTo(null);

        jTextField1.setText(SystemInfo.getMinecraftPath());

        dialogButton.addActionListener(e -> {
            Dialog.showSelectMinecraftFolder();
            jTextField1.setText(SystemInfo.getMinecraftPath());
        });

        jTextField1.setEditable(false);

        clearColorChooser();

        jColorChooser1.setColor(SwingUtils.getColor(0));

        HashMap<String, String> languageMap = new HashMap<>();

        String[] languages = Configuration.getValues("support.languages");

        for(int x = 0; x < languages.length; x++){

            String languageValue = Configuration.getValue("language." + languages[x]);

            languageMap.put(languages[x], languageValue);
            languageMap.put(languageValue, languages[x]);

            languages[x] = languageValue;
        }

        jComboBox1.setModel(new DefaultComboBoxModel<>(languages));
        jComboBox1.setSelectedItem(Configuration.getValue("language." + SystemInfo.getLanguage()));

        jComboBox1.addActionListener(e -> {
            String language = languageMap.get(jComboBox1.getSelectedItem());
            SystemInfo.setLanguage(language);

            Main.frame.getContentPane().removeAll();
            Main.frame.getContentPane().add(new MainPanel());
            SwingUtilities.updateComponentTreeUI(Main.frame);

            SwingUtils.clearAllTabs();

            this.dispose();
            new SettingsFrame();
        });

    }



