    private void clearColorChooser(){
        AbstractColorChooserPanel[] panels = jColorChooser1.getChooserPanels();
        for (AbstractColorChooserPanel panel : panels) {
            if (!panel.getDisplayName().equals("HSV")) {
                jColorChooser1.removeChooserPanel(panel);
            } else {

                JPanel hsvPanelComponent = ((JPanel) panel.getComponents()[0]);
                hsvPanelComponent.remove(3);
                hsvPanelComponent.remove(7);
                hsvPanelComponent.remove(11);
            }
        }
    }

